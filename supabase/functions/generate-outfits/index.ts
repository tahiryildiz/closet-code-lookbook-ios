
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";
import { createOutfitPrompt } from './utils/prompt-generator.ts';
import { processValidatedOutfits } from './utils/enhanced-processor.ts';
import { generateFallbackOutfits } from './utils/fallback-generator.ts';

const openAIApiKey = Deno.env.get('OPENAI_API_KEY');

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { occasion, timeOfDay, weather, wardrobeItems } = await req.json();

    console.log('üöÄ KombinAI: STRICT outfit generation started');
    console.log('üìä Parameters:', { occasion, timeOfDay, weather, wardrobeCount: wardrobeItems?.length });
    
    if (wardrobeItems && wardrobeItems.length > 0) {
      console.log('üëï Wardrobe items:', wardrobeItems.map(item => ({
        name: item.name,
        category: item.category,
        color: item.primary_color || item.color
      })));
    }

    if (!occasion || !timeOfDay || !weather) {
      return new Response(
        JSON.stringify({ error: 'Missing required parameters' }),
        { status: 400, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    if (!wardrobeItems || wardrobeItems.length === 0) {
      return new Response(
        JSON.stringify({ error: 'No wardrobe items provided' }),
        { status: 400, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    if (!openAIApiKey) {
      console.error('‚ùå OpenAI API key not found');
      return new Response(
        JSON.stringify({ error: 'OpenAI API key not configured' }),
        { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      );
    }

    // Create ULTRA-STRICT prompt with exact item names
    const prompt = createOutfitPrompt(wardrobeItems, occasion, timeOfDay, weather);
    console.log('üìù Generated prompt (preview):', prompt.substring(0, 500) + '...');

    console.log('ü§ñ Sending STRICT validation prompt to AI...');

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          { 
            role: 'system', 
            content: 'Sen KombinAI profesyonel stil danƒ±≈ümanƒ±sƒ±n. KESINLIKLE SADECE verilen gardroba √ºr√ºnlerini kullan. √úr√ºn isimlerini TAM OLARAK AYNI ≈ûEKƒ∞LDE yaz. Ba≈üka √ºr√ºn EKLEME veya DEƒûƒ∞≈ûTƒ∞RME. SADECE ge√ßerli JSON d√∂nd√ºr.' 
          },
          { role: 'user', content: prompt }
        ],
        temperature: 0.1, // Minimum temperature for maximum consistency
        max_tokens: 1500,
      }),
    });

    console.log('ü§ñ AI response status:', response.status);

    if (!response.ok) {
      const errorText = await response.text();
      console.error('‚ùå OpenAI API error:', response.status, errorText);
      throw new Error(`OpenAI API error: ${response.status}`);
    }

    const data = await response.json();
    let generatedContent = data.choices[0].message.content;

    console.log('ü§ñ Raw AI response:', generatedContent);

    // Clean JSON response
    generatedContent = generatedContent.trim();
    if (generatedContent.startsWith('```json')) {
      generatedContent = generatedContent.replace(/```json\n?/, '').replace(/\n?```$/, '');
    }
    if (generatedContent.startsWith('```')) {
      generatedContent = generatedContent.replace(/```\n?/, '').replace(/\n?```$/, '');
    }

    try {
      const parsedOutfits = JSON.parse(generatedContent);
      
      if (!parsedOutfits.outfits || parsedOutfits.outfits.length === 0) {
        throw new Error('No outfits generated by AI');
      }

      console.log('üéØ AI generated outfits:', parsedOutfits.outfits.length);
      console.log('üìã Outfit preview:', parsedOutfits.outfits.map(o => ({
        name: o.name,
        items: o.items
      })));

      // Process with ULTRA-STRICT validation
      const validatedOutfits = await processValidatedOutfits(
        parsedOutfits.outfits, 
        wardrobeItems, 
        occasion, 
        timeOfDay, 
        weather, 
        openAIApiKey
      );

      if (validatedOutfits.length === 0) {
        console.log('‚ùå ALL AI OUTFITS FAILED VALIDATION - Using safe fallback');
        const fallbackOutfits = generateFallbackOutfits(wardrobeItems, occasion, weather);
        
        return new Response(JSON.stringify({
          outfits: fallbackOutfits,
          warning: 'AI kombinleri ge√ßersizdi, gardƒ±robunuzdan g√ºvenli kombinler olu≈üturuldu.',
          validation_failed: true
        }), {
          headers: { ...corsHeaders, 'Content-Type': 'application/json' },
        });
      }

      console.log('üéâ STRICT VALIDATION SUCCESSFUL!');
      console.log('‚úÖ Final validated outfits:', validatedOutfits.length);
      
      return new Response(JSON.stringify({ 
        outfits: validatedOutfits,
        validation_passed: true,
        strict_validation: true
      }), {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
      
    } catch (parseError) {
      console.error('‚ùå Failed to parse AI response:', parseError);
      console.log('üõü Using fallback generator due to parse error');
      
      // Enhanced fallback with exact wardrobe items
      const fallbackOutfits = generateFallbackOutfits(wardrobeItems, occasion, weather);

      return new Response(JSON.stringify({
        outfits: fallbackOutfits,
        warning: 'AI servisi kullanƒ±lamadƒ±, gardƒ±robunuzdan g√ºvenli kombinler olu≈üturuldu.',
        fallback_used: true
      }), {
        headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      });
    }
  } catch (error) {
    console.error('‚ùå Error in generate-outfits function:', error);
    return new Response(
      JSON.stringify({ error: 'Internal server error', details: error.message }),
      { status: 500, headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
    );
  }
});
